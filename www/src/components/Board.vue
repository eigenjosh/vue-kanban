<template>
  <div class="container"> 
    Active Board: {{board}}
  </div>
</template>

<script>
export default {
  name: 'board',
  mounted(){
    this.$root.$store.dispatch('getBoard',this.$route.params.id)
  },
  computed:{
    board(){
      return this.$store.state.activeBoard
    }
  }
}
</script>

